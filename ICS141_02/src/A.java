
public class A {
	static double length;
	static double width;
	static double height;
	
	static double getArea(double length,double width){
		double Area;
		Area=length*width;
		return Area;
	}
	
	static double getVolume(double length, double width, double height){
		double Volume;
		Volume=length*width*height;
		return Volume;
	}
}
